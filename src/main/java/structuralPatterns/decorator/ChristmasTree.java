package structuralPatterns.decorator;

/**
 * The ChristmasTree interface defines the contract for decorating a Christmas tree.
 * It provides a method to get the description of the decorated tree.
 */
public interface ChristmasTree {
    /**
     * Decorates the Christmas tree and returns a description of the tree.
     *
     * @return A string description of the decorated Christmas tree.
     */
    String decorate();
}
